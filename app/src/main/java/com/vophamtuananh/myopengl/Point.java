public class Point {

    public int x;
    public int y;
    public int r;
    public int g;
    public int b;

    public Point(int x, int y, int r, int g, int b) {
        this.x = x;
        this.y = y;
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
